public class ifelse {
    public static void main(String[] args) {
        int sayi = 0;

        if (sayi > 0) {
        System.out.println("Sayı pozitiftir.");
        } else if (sayi < 0) {
        System.out.println("Sayı negatiftir.");
        } else {
        System.out.println("Sayı sıfırdır.");
        }
    }
}
